package exemplo02;

public class Exemplo02 {
    public static void main(String[] args) {
        int numero = 0;

        if (numero > 0) {
            System.out.printf("O valor é positivo");
        } else {
            if (numero < 0) {
                System.out.printf("O valor não é positivo");
            } else {
                System.out.printf("O valor é zero");
            }

        }
    }
}
